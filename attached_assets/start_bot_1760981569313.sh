        echo "   - EMBY_SERVER_URL (–∞–¥—Ä–µ—Å –≤–∞—à–µ–≥–æ Emby —Å–µ—Ä–≤–µ—Ä–∞)"
        echo "   - EMBY_API_KEY (API –∫–ª—é—á –∏–∑ –Ω–∞—Å—Ç—Ä–æ–µ–∫ Emby)"
